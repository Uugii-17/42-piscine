int ft_iterative_factorial(int nb)
{
	int result;
	int i;

	if ((nb < 0) || (nb > 12))
		return (0);

	result = 1;
	i = 0;
	while (i < nb)
	{
		result *= i + 1;
		i++;
	}
	return (result);
}

int	ft_iterative_factorial(int nb)
{
	int res;
	int i;

	if ((nb < 0) || (nb > 12))
		return (0);
	res = 1;
	i = 0;
	while (i < nb)
	{
		res *= i + 1;
		i++;
	}
	return (res);
}
